<h2>SmallURL and your Privacy</h2>
<p>You may be wondering what we store and what we dont and what we do with stored data.</p>
<p>We're here to answer this question!</p>
<p/>
<p>SmallURL stores three key pieces of data relating to you</p>
<p>this data is only stored when you Smallify a URL or click a URL</p>
<p>We store your User Agent, IP Address and Geo Location (Derived from IP)</p>
<p>We use this data only to provide in depth statistics about a URL and help prevent abuse to the services.</p>
<p/>
<h3>Browser Agent<br/><small>What do we do with it?</small></h3>
<p>We save your browser agent to help generate Statistics so we and others can gain in insight as to what is most popular amongst users.</p>
<hr>
<h3>IP Address</h3>
<p>Your IP Address is stored by SmallURL so we can generate a Geo Location point from it as well use IP Adresses to keep a look out for service abuse, abusive users can easily be found in our Database and be blacklisted from abusing the service.</p>
<p>Service abusing uses our resources, the more resources we have the faster SmallURL is for you.</p>
<p>Only two members of the Team have access to such data and are under a ZERO-TOLERANCE Policy on using the data for anything other than improving SmallURL</p>
<p>The Data is kept SECURE and is not viewable by anyone other than the allowed Team Members.</p>
<p/>
<h3>Geo Location</h3>
<p>Your Geo Location is derived from your IP's location. This is most certainly always your ISP's Location and not your house location, we do not use your browsers Geo Location capability to get this information (See APIs on About Page)</p>
<p>All Geo Location data is then collected into one large set of data and is converted into a HeatMap which displays SmallURL popularity across the world.</p>
<p>This data CANNOT be reverse engineered to retrieve your house address or IP.</p>
<p/>
<hr>
<h2>SmallURL and Others</h2>
<p>As others have thought recently about other sites that save data, They wonder if private data is sold to other companies or not.</p>
<p>SmallURL does NOT sell or give out any saved DATA containing any of the above, It is kept soley in our databases and is not viewable by anyone other than SmallURls Tech Team</p>
<p>Under NO circumstances will SmallURL give you other peoples information or even your own upon request. Such requests will be REFUSED.</p>
<p>SmallURL will not work with other companies that ask for such data, Only Law Enforcements may request such data but first must be verified that they are real.</p>
<p>SmallURL agrees to only use such data to stop lethal/illegal activities.</p>
<p/>
<h3>Bad URLs, Illegal URLs. What happens?</h3>
<p>Nothing will happen to a URL even if it links to Illegal contenct.</p>
<p>We DO NOT remove illegal URLs or sexually explicit URLs and only agree to remove URLs that link to ANY form of Child Pornography or Murder related Pictures/Videos/Sites.</p>
